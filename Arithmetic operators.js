//Arithmetic operators
const num1 = 100;
const num2 = 50;
let val;

//Addition
val = num1 + num2; //150

//Subtraction
val = num1 - num2; //50

//Multiply
val = num1 * num2; //5000

//Divide
val = num1 / num2; //2

//Modulus
val = num1 % num2; //0


//Increment - Decrement
val = 1;
val = val + 1; //2
val++; //3
val--; //2
--val;



console.log(val);

